public class Edge {
    private String vi;
    private String vj;

    public Edge(String vi, String vj) {
        this.vi = vi;
        this.vj = vj;
    }

    public Edge getEdge() {
        return this;
    }

    public String getVi() {
        return vi;
    }

    public String getVj() {
        return vj;
    }

}
